# Demonstration of Python data types

# Integer
age = 25

# Float
height = 1.75

# String
name = "Alice"

# Boolean
is_student = True

# List
scores = [85, 90, 78]

# Tuple
coordinates = (10.0, 20.0)

# Set
unique_numbers = {1, 2, 3, 2}

# Dictionary
person = {
    "name": name,
    "age": age,
    "height": height,
    "is_student": is_student
}

# Print all variables and their types
print("age:", age, type(age))
print("height:", height, type(height))
print("name:", name, type(name))
print("is_student:", is_student, type(is_student))
print("scores:", scores, type(scores))
print("coordinates:", coordinates, type(coordinates))
print("unique_numbers:", unique_numbers, type(unique_numbers))
print("person:", person, type(person))